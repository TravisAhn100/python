"""""""""""""""""""""""""""""""""""""""
constant time(무반복) solution
uses fact that:
                           n * (n + 1)
      1 + 2 + ... + n   =  -----------
                               2

"""""""""""""""""""""""""""""""""""""""
upbound = 1000 - 1 # 1000 포함 안 하므로 빼줍니다
sum_3s  =  3 * (upbound //  3) * ((upbound //  3) + 1) // 2
sum_5s  =  5 * (upbound //  5) * ((upbound //  5) + 1) // 2
sum_15s = 15 * (upbound // 15) * ((upbound // 15) + 1) // 2
sum     = (sum_3s + sum_5s - sum_15s)
"""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""
"""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""""
t = time() - t
print(f"{sum} 소요시간: {t:.6f}초")


